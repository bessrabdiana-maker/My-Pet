function goToForm(name, age, gender) {
  const url = `forma.html?name=${encodeURIComponent(name)}&age=${encodeURIComponent(age)}&gender=${encodeURIComponent(gender)}`;
  window.location.href = url;
}


async function loadXML() {
    const response = await fetch("dogs.xml");
    const xmlText = await response.text();
    const parser = new DOMParser();
    const xml = parser.parseFromString(xmlText, "text/xml");

    const dogs = xml.getElementsByTagName("dog");
    const container = document.getElementById("dogs");

    for (let dog of dogs) {
        let name = dog.getElementsByTagName("name")[0].textContent;
        let gender = dog.getElementsByTagName("gender")[0].textContent;
        let age = dog.getElementsByTagName("age")[0].textContent;
        let breed = dog.getElementsByTagName("breed")[0].textContent;

        let photos = [...dog.getElementsByTagName("photo")].map(p => p.textContent);

        // создаём HTML-карточку
        let card = document.createElement("article");
        card.innerHTML = `
            <img class="myImage"
                 src="${photos[0]}"
                 data-photos="${photos.join(',')}"
                 data-index="0"
                 onclick="changeImage(this)">
            <div class="rectangle2">
                <div>
                    <h2>${name}</h2>
                    <ul>
                        <li><b>Пол:</b> ${gender}</li>
                        <li><b>Возраст:</b> ${age}</li>
                        <li><b>Вид:</b> Собака</li>
                        <li><b>Порода:</b> ${breed}</li>
                    </ul>
                </div>
                <div class="btn-block">
                    <button onclick="showInfo('${name}')">Подробнее...</button>
                    <button onclick="goToForm('${name}','${age}','${gender}')">Оставить заявку</button>
                </div>
            </div>
        `;
        container.appendChild(card);
    }
}

loadXML();
