function goToForm(name, age, gender) {
  const url = `forma.html?name=${encodeURIComponent(name)}&age=${encodeURIComponent(age)}&gender=${encodeURIComponent(gender)}`;
  window.location.href = url;
}

async function loadXML() {
    const response = await fetch("cats.xml");
    const xmlText = await response.text();
    const xml = new DOMParser().parseFromString(xmlText, "text/xml");

    const cats = xml.getElementsByTagName("cat");
    const container = document.getElementById("cats");

    for (let cat of cats) {
        let name = cat.getElementsByTagName("name")[0].textContent;
        let gender = cat.getElementsByTagName("gender")[0].textContent;
        let age = cat.getElementsByTagName("age")[0].textContent;
        let breed = cat.getElementsByTagName("breed")[0].textContent;

        let photos = [...cat.getElementsByTagName("photo")].map(p => p.textContent);

        let card = document.createElement("article");
        card.innerHTML = `
            <img class="myImage"
                 src="${photos[0]}"
                 data-photos="${photos.join(',')}"
                 data-index="0"
                 onclick="changeImage(this)">
            <div class="rectangle2">
                <div>
                    <h2>${name}</h2>
                    <ul>
                        <li><b>Пол:</b> ${gender}</li>
                        <li><b>Возраст:</b> ${age}</li>
                        <li><b>Вид:</b> Кот</li>
                        <li><b>Порода:</b> ${breed}</li>
                    </ul>
                </div>
                <div class="btn-block">
                    <button onclick="showInfo('${name}')">Подробнее...</button>
                    <button onclick="goToForm('${name}','${age}','${gender}')">Оставить заявку</button>
                </div>
            </div>
        `;
        container.appendChild(card);
    }
}

